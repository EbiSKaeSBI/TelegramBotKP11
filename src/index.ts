function validateEnvironmentVariables() {
    const requiredVars = [
        'TELEGRAM_BOT_TOKEN',
        'FLOWISE_API_URL',
        'FLOWISE_API_KEY'
    ];
    
    const missingVars = requiredVars.filter(varName => !process.env[varName]);
    
    if (missingVars.length > 0) {
        console.error('‚ùå –û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è:');
        missingVars.forEach(varName => {
            console.error(`   - ${varName}`);
        });
        console.error('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ —Ñ–∞–π–ª .env');
        return false;
    }
    
    console.log('‚úÖ –í—Å–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –∑–∞–≥—Ä—É–∂–µ–Ω—ã');
    console.log(`ü§ñ Telegram Bot Token: ${process.env.TELEGRAM_BOT_TOKEN ? '‚úì' : '‚úó'}`);
    console.log(`üîó Flowise API URL: ${process.env.FLOWISE_API_URL ? '‚úì' : '‚úó'}`);
    console.log(`üîë Flowise API Key: ${process.env.FLOWISE_API_KEY ? '‚úì' : '‚úó'}`);
    
    return true;
}

// –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞
